package ipy_info;

public class Ipy_info {
	private String ipy_account;
	private String ipy_admin;
	private int ipy_total;
	private String ipy_name;
	
	
	public String getIpy_account() {
		return ipy_account;
	}
	public void setIpy_account(String ipy_account) {
		this.ipy_account = ipy_account;
	}
	public String getIpy_admin() {
		return ipy_admin;
	}
	public void setIpy_admin(String ipy_admin) {
		this.ipy_admin = ipy_admin;
	}
	public int getIpy_total() {
		return ipy_total;
	}
	public void setIpy_total(int ipy_total) {
		this.ipy_total = ipy_total;
	}
	public String getIpy_name() {
		return ipy_name;
	}
	public void setIpy_name(String ipy_name) {
		this.ipy_name = ipy_name;
	}
	
	
}
